export const astTransformCode = [
  "// packages/ast-walker/src/walker.ts",
  "import type { ElementNode, RootNode, TemplateChildNode } from '@vue/compiler-core'",
  "import { NodeTypes, ElementTypes, baseParse } from '@vue/compiler-core'",
  "",
  "export interface AttrInjection {",
  "  offset: number      // 插入位置",
  "  attr: string         // 属性字符串",
  "  tag: string          // 节点标签名",
  "  line: number         // 行号 (1-based)",
  "  column: number       // 列号 (0-based)",
  "}",
  "",
  "const SKIP_TAGS = new Set([",
  "  'template', 'slot', 'component',",
  "  'transition', 'transition-group',",
  "  'keep-alive', 'teleport', 'suspense',",
  "])",
  "",
  "export function walkTemplateAST(",
  "  templateSource: string,",
  "  filePath: string,",
  "  attrName = 'data-source-file',",
  "): AttrInjection[] {",
  "  const ast = baseParse(templateSource, { comments: true })",
  "  const injections: AttrInjection[] = []",
  "  walkNode(ast, filePath, attrName, injections)",
  "  return injections",
  "}",
  "",
  "function walkNode(node, filePath, attrName, injections) {",
  "  switch (node.type) {",
  "    case NodeTypes.ELEMENT: {",
  "      const el = node as ElementNode",
  "      if (!SKIP_TAGS.has(el.tag)) {",
  "        const { line, column } = el.loc.start",
  "        injections.push({",
  "          offset: findOpenTagEnd(el),",
  "          attr: ` ${attrName}=\"${filePath}:${line}:${column}\"`,",
  "          tag: el.tag, line, column,",
  "        })",
  "      }",
  "      el.children.forEach(c => walkNode(c, filePath, attrName, injections))",
  "      break",
  "    }",
  "    case NodeTypes.ROOT:",
  "      node.children.forEach(c => walkNode(c, filePath, attrName, injections))",
  "      break",
  "    case NodeTypes.IF:  // v-if",
  "      node.branches.forEach(b =>",
  "        b.children.forEach(c => walkNode(c, filePath, attrName, injections)))",
  "      break",
  "    case NodeTypes.FOR: // v-for",
  "      node.children.forEach(c => walkNode(c, filePath, attrName, injections))",
  "      break",
  "  }",
  "}",
  "",
  "function findOpenTagEnd(el: ElementNode): number {",
  "  const s = el.loc.source",
  "  return el.loc.start.offset + (el.isSelfClosing ? s.indexOf('/>') : s.indexOf('>'))",
  "}",
].join('\n');

