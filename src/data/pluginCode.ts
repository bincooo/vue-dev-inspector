export const vitePluginCode = [
  "// packages/vite-plugin-vue-dev-inspector/src/plugin.ts",
  "import type { Plugin } from 'vite'",
  "import { parse, compileTemplate } from '@vue/compiler-sfc'",
  "import MagicString from 'magic-string'",
  "import type { DevInspectorOptions } from './options'",
  "import { DEFAULT_OPTIONS } from './options'",
  "import { createInspectorTransform } from './transform'",
  "import { generateClientScript } from './client'",
  "",
  "export function vueDevInspector(",
  "  userOptions: DevInspectorOptions = {}",
  "): Plugin {",
  "  const options = { ...DEFAULT_OPTIONS, ...userOptions }",
  "  let isDev = false",
  "  let projectRoot = ''",
  "",
  "  return {",
  "    name: 'vue-dev-inspector',",
  "    enforce: 'pre',",
  "",
  "    configResolved(config) {",
  "      isDev = config.command === 'serve'",
  "      projectRoot = config.root",
  "    },",
  "",
  "    transform(code, id) {",
  "      if (!isDev || !options.enabled) return null",
  "      if (!id.endsWith('.vue')) return null",
  "      if (options.exclude.some(re => re.test(id))) return null",
  "",
  "      const { descriptor, errors } = parse(code, { filename: id })",
  "      if (errors.length || !descriptor.template) return null",
  "",
  "      const template = descriptor.template",
  "      const s = new MagicString(code)",
  "      const relativePath = id.replace(projectRoot + '/', '')",
  "",
  "      compileTemplate({",
  "        source: template.content,",
  "        filename: id,",
  "        id,",
  "        compilerOptions: {",
  "          nodeTransforms: [",
  "            createInspectorTransform(s, template, relativePath, options.attrName),",
  "          ],",
  "        },",
  "      })",
  "",
  "      if (s.hasChanged()) {",
  "        return {",
  "          code: s.toString(),",
  "          map: s.generateMap({ source: id, includeContent: true, hires: true }),",
  "        }",
  "      }",
  "      return null",
  "    },",
  "",
  "    transformIndexHtml(html) {",
  "      if (!isDev || !options.enabled) return html",
  "      return html.replace('</body>', generateClientScript(options) + '\\n</body>')",
  "    },",
  "  }",
  "}",
  "",
  "export default vueDevInspector",
].join('\n');

